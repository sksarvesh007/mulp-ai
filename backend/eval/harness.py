"""Eval harness — runs all 12 cases from docs/test_cases.json through the graph,
compares against the expected outcomes, and renders docs/EVAL_REPORT.md with the
full decision trace for each case.

Usage:  uv run python -m eval.harness          # writes the report + prints summary
"""

from __future__ import annotations

import asyncio
import json
import re
import sys
from pathlib import Path
from typing import Any

from pydantic import BaseModel

from app.core.config import get_settings
from app.graph import run_claim
from app.schemas.claim import ClaimInput
from app.schemas.decision import ClaimResult


class CaseOutcome(BaseModel):
    case_id: str
    case_name: str
    passed: bool
    decision: str | None
    approved_amount: int | None
    rejection_reasons: list[str]
    confidence: float | None
    expected: dict[str, Any]
    failures: list[str] = []
    notes: list[str] = []
    mismatch: list[str] = []
    result: ClaimResult


def _conf_threshold(text: str | None) -> float | None:
    if not text:
        return None
    m = re.search(r"([0-9]*\.?[0-9]+)", text)
    return float(m.group(1)) if m else None


def _evaluate(tc: dict[str, Any], result: ClaimResult) -> CaseOutcome:
    exp = tc["expected"]
    d = result.decision
    dec = d.decision.value if d.decision else None
    mismatch: list[str] = []

    if exp.get("decision") != dec:
        mismatch.append(f"decision: got {dec}, expected {exp.get('decision')}")
    if "approved_amount" in exp and exp["approved_amount"] is not None and exp["approved_amount"] != d.approved_amount:
        mismatch.append(f"approved_amount: got {d.approved_amount}, expected {exp['approved_amount']}")
    if exp.get("rejection_reasons"):
        missing = set(exp["rejection_reasons"]) - set(d.rejection_reasons)
        if missing:
            mismatch.append(f"rejection_reasons missing {sorted(missing)} (got {d.rejection_reasons})")
    threshold = _conf_threshold(exp.get("confidence_score"))
    if threshold is not None and (d.confidence is None or d.confidence <= threshold):
        mismatch.append(f"confidence: got {d.confidence}, expected > {threshold}")

    return CaseOutcome(
        case_id=tc["case_id"],
        case_name=tc["case_name"],
        passed=not mismatch,
        decision=dec,
        approved_amount=d.approved_amount,
        rejection_reasons=d.rejection_reasons,
        confidence=d.confidence,
        expected=exp,
        failures=[f.component for f in d.component_failures],
        notes=d.notes,
        mismatch=mismatch,
        result=result,
    )


async def run_eval() -> list[CaseOutcome]:
    settings = get_settings()
    cases = json.loads(settings.test_cases_file.read_text())["test_cases"]
    outcomes: list[CaseOutcome] = []
    for tc in cases:
        claim = ClaimInput(**tc["input"], mode="eval")
        result = await run_claim(claim, claim_id=tc["case_id"])
        outcomes.append(_evaluate(tc, result))
    return outcomes


# ── report rendering ───────────────────────────────────────────────────────────
def _fmt_decision(o: CaseOutcome) -> str:
    d = o.result.decision
    if d.decision is None:
        return f"NEEDS_MEMBER_ACTION ({d.document_problem.problem_type.value if d.document_problem else '—'})"
    parts = [d.decision.value]
    if d.approved_amount is not None:
        parts.append(f"₹{d.approved_amount:,}")
    if d.rejection_reasons:
        parts.append(", ".join(d.rejection_reasons))
    return " · ".join(parts)


def render_report(outcomes: list[CaseOutcome]) -> str:
    n_pass = sum(o.passed for o in outcomes)
    total = len(outcomes)
    lines: list[str] = []
    lines.append("# Eval Report — Plum Claims Processing\n")
    lines.append(
        f"**Result: {n_pass}/{total} cases passed.** Generated by `eval/harness.py` "
        f"(all cases run through the LangGraph pipeline in deterministic eval mode).\n"
    )
    lines.append("## Summary\n")
    lines.append("| Case | Name | Produced | Expected | Conf | Pass |")
    lines.append("|------|------|----------|----------|------|------|")
    for o in outcomes:
        exp = o.expected
        exp_str = exp.get("decision") or "stop (NEEDS_MEMBER_ACTION)"
        if exp.get("approved_amount") is not None:
            exp_str += f" · ₹{exp['approved_amount']:,}"
        if exp.get("rejection_reasons"):
            exp_str += " · " + ", ".join(exp["rejection_reasons"])
        lines.append(
            f"| {o.case_id} | {o.case_name} | {_fmt_decision(o)} | {exp_str} | "
            f"{o.confidence if o.confidence is not None else '—'} | {'✅' if o.passed else '❌'} |"
        )
    lines.append("")

    lines.append("## Per-case detail (decision + full trace)\n")
    for o in outcomes:
        d = o.result.decision
        lines.append(f"### {o.case_id} — {o.case_name}  {'✅' if o.passed else '❌'}\n")
        if o.mismatch:
            lines.append("**Mismatch:** " + "; ".join(o.mismatch) + "\n")
        lines.append(f"- **Decision:** {_fmt_decision(o)}")
        if d.reason:
            lines.append(f"- **Reason:** {d.reason}")
        if d.eligible_from:
            lines.append(f"- **Eligible from:** {d.eligible_from}")
        if d.document_problem:
            lines.append(f"- **Member message:** {d.document_problem.message}")
        if d.financial_breakdown:
            fb = d.financial_breakdown
            lines.append(
                f"- **Financial:** base ₹{fb.base:,} → discount ₹{fb.network_discount:,} "
                f"→ after ₹{fb.after_discount:,} → co-pay ₹{fb.copay:,} → final ₹{fb.final:,}"
                + (f" (caps: {'; '.join(fb.clamps)})" if fb.clamps else "")
            )
        if d.line_items:
            lines.append("- **Line items:**")
            for li in d.line_items:
                lines.append(
                    f"    - {li.description}: ₹{li.amount:,} → {li.status.value}"
                    + (f" ({li.reason})" if li.reason else "")
                )
        if d.fraud_signals:
            lines.append("- **Fraud signals:** " + "; ".join(s.detail for s in d.fraud_signals))
        if d.degraded:
            lines.append(f"- **Degraded:** yes — failed components: {', '.join(o.failures)}")
        if d.confidence is not None:
            lines.append(
                f"- **Confidence:** {d.confidence}"
                + (
                    f" — {'; '.join(f'{x.reason} ({x.delta:+})' for x in d.confidence_breakdown.deltas)}"
                    if d.confidence_breakdown and d.confidence_breakdown.deltas
                    else ""
                )
            )
        if d.notes:
            lines.append("- **Notes:** " + "; ".join(d.notes))
        lines.append("\n<details><summary>Full trace</summary>\n")
        for e in o.result.trace:
            ref = f" [{e.policy_ref}]" if e.policy_ref else ""
            lines.append(f"- `{e.status.value.upper():4}` **{e.step}** — {e.detail}{ref}")
        lines.append("\n</details>\n")
    return "\n".join(lines)


def write_report(outcomes: list[CaseOutcome], path: Path | None = None) -> Path:
    out = path or (get_settings().policy_file.parent / "EVAL_REPORT.md")
    out.write_text(render_report(outcomes), encoding="utf-8")
    return out


def main() -> int:
    outcomes = asyncio.run(run_eval())
    path = write_report(outcomes)
    n_pass = sum(o.passed for o in outcomes)
    for o in outcomes:
        print(f"{'✅' if o.passed else '❌'} {o.case_id} {o.case_name}")
        for m in o.mismatch:
            print(f"     ↳ {m}")
    print(f"\n{n_pass}/{len(outcomes)} passed → report written to {path}")
    if "--langfuse" in sys.argv:
        from app.observability.langfuse_eval import log_eval_run

        log_eval_run(outcomes)
        print("Logged eval run to Langfuse.")
    return 0 if n_pass == len(outcomes) else 1


if __name__ == "__main__":
    raise SystemExit(main())
