"""Live LLM eval — runs all 12 cases through the pipeline in **live** mode, so the
LLM (OCR → DeepSeek) does the entire perception step on generated document images.
Metadata that isn't in a document (member, amount, claims history, the TC011 failure
knob) comes from the case input; document fields are extracted by the model.

Usage:  uv run python -m eval.live_harness
"""

from __future__ import annotations

import asyncio
import json
from typing import Any

from app.core.config import get_settings
from app.graph import run_claim
from app.schemas.claim import ClaimInput
from app.schemas.decision import ClaimResult
from eval.docgen import generate_documents


def build_live_claim(case: dict[str, Any]) -> ClaimInput:
    meta = {k: v for k, v in case["input"].items() if k != "documents"}
    docs = generate_documents(case)
    return ClaimInput(**meta, documents=docs, mode="live")


def check(case: dict[str, Any], result: ClaimResult) -> list[str]:
    exp = case["expected"]
    d = result.decision
    dec = d.decision.value if d.decision else None
    issues: list[str] = []
    if exp.get("decision") != dec:
        issues.append(f"decision got={dec} want={exp.get('decision')}")
    if exp.get("approved_amount") is not None and exp["approved_amount"] != d.approved_amount:
        issues.append(f"amount got={d.approved_amount} want={exp['approved_amount']}")
    if exp.get("rejection_reasons"):
        missing = set(exp["rejection_reasons"]) - set(d.rejection_reasons)
        if missing:
            issues.append(f"reasons missing={sorted(missing)} got={d.rejection_reasons}")
    return issues


async def main() -> int:
    cases = json.loads(get_settings().test_cases_file.read_text())["test_cases"]
    rows: list[tuple[str, str, str, str, bool, list[str]]] = []
    n_pass = 0
    for case in cases:
        label, amt, issues = "ERROR", None, []
        try:
            result = await run_claim(build_live_claim(case), claim_id=case["case_id"])
            issues = check(case, result)
            d = result.decision
            label = d.decision.value if d.decision else "NEEDS_ACTION"
            amt = d.approved_amount
        except Exception as exc:  # noqa: BLE001
            issues = [f"EXCEPTION {type(exc).__name__}: {exc}"]
        ok = not issues
        n_pass += ok
        exp = case["expected"]
        exp_str = exp.get("decision") or "stop (NEEDS_MEMBER_ACTION)"
        if exp.get("approved_amount") is not None:
            exp_str += f" / ₹{exp['approved_amount']:,}"
        rows.append(
            (case["case_id"], case["case_name"], f"{label}{f' / ₹{amt:,}' if amt else ''}", exp_str, ok, issues)
        )
        print(f"{'✅' if ok else '❌'} {case['case_id']} {case['case_name'][:32]:32} -> {label:16} amt={amt}")
        for it in issues:
            print(f"      ↳ {it}")
    print(f"\nLIVE LLM: {n_pass}/{len(cases)} passed")
    _write_report(rows, n_pass)
    return 0 if n_pass == len(cases) else 1


def _write_report(rows: list[tuple[str, str, str, str, bool, list[str]]], n_pass: int) -> None:
    out = get_settings().policy_file.parent / "EVAL_REPORT_LIVE.md"
    lines = [
        "# Live LLM Eval Report",
        "",
        f"**Result: {n_pass}/{len(rows)} passed via the live AI path.** Each case's documents are "
        "rendered to images, then processed by the *live* pipeline — **Tesseract OCR → DeepSeek "
        "(classify + extract) → deterministic adjudication** — with no pre-extracted content. "
        "Generated by `eval/live_harness.py`.",
        "",
        "| Case | Name | Produced (live LLM) | Expected | Pass |",
        "|------|------|---------------------|----------|------|",
    ]
    for cid, name, produced, expected, ok, issues in rows:
        note = (" — " + "; ".join(issues)) if issues else ""
        lines.append(f"| {cid} | {name} | {produced} | {expected} | {'✅' if ok else '❌' + note} |")
    out.write_text("\n".join(lines) + "\n", encoding="utf-8")


if __name__ == "__main__":
    raise SystemExit(asyncio.run(main()))
